#include "../../includes/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*pointer;

	if (!(pointer = malloc(count * size)))
		return (NULL);
	ft_bzero(pointer, count * size);
	return (pointer);
}
